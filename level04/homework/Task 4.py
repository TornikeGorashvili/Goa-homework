# მაგალითი 1: ორი რიცხვის დამატება
a = 5  # პირველი რიცხვი
b = 3  # მეორე რიცხვი
total = a + b  # ორი რიცხვის ჯამი
print("a + b =", total)  # დაბეჭდავს a და b-ის ჯამს

# მაგალითი 2: ორი რიცხვის ნამრავლი და სხვაობა
product = a * b  # ორი რიცხვის ნამრავლი
difference = a - b  # ორი რიცხვის სხვაობა

print("a * b =", product)  # დაბეჭდავს a და b-ის ნამრავლს
print("a - b =", difference)  # დაბეჭდავს a და b-ის სხვაობას

