# ჩვეულებრივი გაყოფა (/) ყოველთვის აბრუნებს float ტიპის შედეგს
x = 10 / 3  # 'x' მიიღებს მნიშვნელობას 3.3333333333333335 (float ტიპის)
print("ჩვეულებრივი გაყოფა (10 / 3):", x)

# მთელი რიცხვების გაყოფა (//) აბრუნებს მთელ რიცხვს, რომელიც არის შედეგის ყველაზე დიდი მთელი რიცხვი
y = 10 // 3  # 'y' მიიღებს მნიშვნელობას 3 (int ტიპის)
print("მთელი რიცხვების გაყოფა (10 // 3):", y)
